    private void getSalesByDay() {
        if (verifyFields()) {
            Date dateSearch = dpkSearchDay.getDate();
            ResponseModel<List<Sale>> response = new SaleService().getSalesByDay(dateSearch);
            this.listSales = response.getModel();
            
            this.setSaleTable();
        }
    }
    
    private void setSaleTable() {
        this.table = (DefaultTableModel)tableSale.getModel();
        this.table.setRowCount(0);
        this.totalValue = 0d;
        
        this.listSales.stream().forEach(sale -> {
            String amount;
            
            if (sale.getProduct().getMeasureType().getCalcType().getId() == CalcTypeEnum.UNITY.value) {
                amount = String.valueOf((int)sale.getAmount());
            } else {
                amount = FormatMoney.verifyDecimal(String.valueOf(sale.getAmount()), CalcTypeEnum.WEIGHT.value);
            }
            
            this.table.addRow(new Object[] {
                sale.getProduct().getProductName(),
                "R$ " + this.df.format(sale.getProduct().getProductValue()),
                amount,
                "R$ " + this.df.format(sale.getSaleTotal())
            });
            
            this.totalValue += sale.getSaleTotal();
        });
        
        String totalValueString = this.df.format(this.totalValue);
        lblTotalSale.setText("R$ " + totalValueString);
    }
    
    private boolean verifyFields() {
        boolean isVerify = true;
        String message = "";
        
        if (dpkSearchDay.getDate().toString().isEmpty()) {
            message += Messages.date_required + "\n";
        }
        
        if (dpkSearchDay.getDate().after(Date.from(Instant.now()))) {
            message += Messages.date_invalid + "\n";
        }
        
        if (!message.isEmpty()) {
            JOptionPane.showMessageDialog(null, message);
            isVerify = false;
        }
        
        return isVerify;
    }

